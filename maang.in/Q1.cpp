void solve() {
    int n;
    cin >> n;

    vector<int> arr(n);
    for (int i = 0; i < n; i++) {
        cin >> arr[i];
    }
    
    vector<int> lis(n, 1);
    vector<int> countLis(n, 1);
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < i; j++) {
            if (arr[j] < arr[i]) {
                lis[i] = lis[j] + 1;
            }
        }

        int ctr = 0;
        for (int j = 0; j < i; j++) {
            if (arr[j] < arr[i] && lis[j] == lis[i] - 1) {
                ctr++;
            }
        }
        countLis[i] = ctr;
    }

    pr(arr);
    pr(lis);

    int lengthLis = 1;
    for (int i = 0; i < n; i++) {
        lengthLis = max(lengthLis, lis[i]);
    }

    int ans = 0;
    for (int i = 0; i < n; i++) {
        if (lis[i] == lengthLis) {
            ans += countLis[i];
        }
    }

    cout << ans << endl;
}

